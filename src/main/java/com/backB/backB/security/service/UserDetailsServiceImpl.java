package com.backB.backB.security.service;

import com.backB.backB.entity.Usuario;
import com.backB.backB.service.UsuarioService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

/**
 *
 * @author jarellano22
 */
@Service
public class UserDetailsServiceImpl implements UserDetailsService {

    @Autowired
    UsuarioService usuarioService;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {

        Usuario usuario = usuarioService.getByUsername(username).orElseThrow(() -> new UsernameNotFoundException("No existe el usuario"));

        return UsuarioPrincipal.build(usuario);
    }
}
